package day13_forLoop;

public class C11_NestefForLoop {
    public static void main(String[] args) {

        for (int i = 1; i <=4 ; i++) {
            System.out.print(i+" ");
        }
        System.out.println("");


        for (int i = 1; i <=4 ; i++) {
            System.out.println(i*2+" ");
            
        }
        System.out.println("");

        for (int i = 1; i <=4 ; i++) {
            System.out.println(i*3+" ");
        }
        System.out.println("");








    }
}
